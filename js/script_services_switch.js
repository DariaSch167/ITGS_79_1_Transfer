// Функция для переключения пунктов отправления и прибытия в модальном окне 1
function switchPoints1() {
    const departureSelect = document.getElementById("departurePoint1");
    const arrivalSelect = document.getElementById("arrivalPoint1");
    const tempDepartureValue = departureSelect.value;
    const tempArrivalValue = arrivalSelect.value;

    departureSelect.value = tempArrivalValue;
    arrivalSelect.value = tempDepartureValue;
}

